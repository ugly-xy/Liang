package com.we.models.article;

import com.we.common.Constant.Const;
import com.we.models.AbstractDocument;

public class TipOffSt extends AbstractDocument {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6016361050004072890L;

	private int count;
	private int status = Const.STATUS_DEF;

	public TipOffSt() {

	}

	public TipOffSt( Long id, int count) {
		this._id = id;
		this.count = count;
		super.updateTime = updateTime;
	}

	
	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}
	
}