package com.we.models.article;

import com.we.models.AbstractDocument;

public class CollectArticle extends AbstractDocument{

	/**
	 * 
	 */
	private static final long serialVersionUID = -7742717296664151310L;
	
	public String title;
	public String content;
	public String conDateStr;
	public Long   conDate;
	public String conTime;
	public String markObj;
	public boolean mark;
	public int from;
	public int status;
	public String des;
	
	public Long createTime;
	
	public CollectArticle() {
		super();
	}


	public CollectArticle(String title, String content, String conDateStr, Long conDate, String conTime, String markObj,
			boolean mark,int from, String des) {
		super();
		this.title = title;
		this.content = content;
		this.conDateStr = conDateStr;
		this.conDate = conDate;
		this.conTime = conTime;
		this.markObj = markObj;
		this.mark = mark;
		this.from = from;
		this.des = des;
		this.createTime = System.currentTimeMillis();
		this.updateTime = System.currentTimeMillis();
	}


	public String getTitle() {
		return title;
	}


	public void setTitle(String title) {
		this.title = title;
	}


	public String getContent() {
		return content;
	}


	public void setContent(String content) {
		this.content = content;
	}


	public String getConDateStr() {
		return conDateStr;
	}


	public void setConDateStr(String conDateStr) {
		this.conDateStr = conDateStr;
	}


	public Long getConDate() {
		return conDate;
	}


	public void setConDate(Long conDate) {
		this.conDate = conDate;
	}


	public String getConTime() {
		return conTime;
	}


	public void setConTime(String conTime) {
		this.conTime = conTime;
	}


	public String getMarkObj() {
		return markObj;
	}


	public void setMarkObj(String markObj) {
		this.markObj = markObj;
	}


	public boolean isMark() {
		return mark;
	}


	public void setMark(boolean mark) {
		this.mark = mark;
	}


	public int getFrom() {
		return from;
	}


	public void setFrom(int from) {
		this.from = from;
	}


	public int getStatus() {
		return status;
	}


	public void setStatus(int status) {
		this.status = status;
	}


	public String getDes() {
		return des;
	}


	public void setDes(String des) {
		this.des = des;
	}


	public Long getCreateTime() {
		return createTime;
	}


	public void setCreateTime(Long createTime) {
		this.createTime = createTime;
	}



}
