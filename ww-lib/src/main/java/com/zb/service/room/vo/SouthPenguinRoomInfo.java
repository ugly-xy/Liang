package com.zb.service.room.vo;

import java.util.ArrayList;
import java.util.List;
import java.util.Map;

public class SouthPenguinRoomInfo extends RoomInfo {
	public SouthPenguinRoomInfo(int type) {
		super(type);
	}

	private static final long serialVersionUID = -2720778956961985644L;
	private List penguins = new ArrayList<>(); // 场上企鹅
	private int inning;// 第几回合
	private int iceSize;// 冰面大小

	public List getPenguins() {
		return penguins;
	}

	public void setPenguins(List penguins) {
		this.penguins = penguins;
	}

	public int getInning() {
		return inning;
	}

	public void setInning(int inning) {
		this.inning = inning;
	}

	public int getIceSize() {
		return iceSize;
	}

	public void setIceSize(int iceSize) {
		this.iceSize = iceSize;
	}
}
