package com.zb.service.room.vo;

import java.util.List;

import com.mongodb.DBObject;

public class MyStarInfo {

	private int slaveLevel = 1;
	private Long worth = 100L;
	private int starLevel = 1;
	private List<DBObject> slave;
	private Long masterId;
	private String masterHead;
	private String masterNickname;
	private int availableBalance;
	private long now = System.currentTimeMillis();

	public String getMasterNickname() {
		return masterNickname;
	}

	public void setMasterNickname(String masterNickname) {
		this.masterNickname = masterNickname;
	}

	public long getNow() {
		return now;
	}

	public void setNow(long now) {
		this.now = now;
	}

	public int getAvailableBalance() {
		return availableBalance;
	}

	public void setAvailableBalance(int availableBalance) {
		this.availableBalance = availableBalance;
	}

	public String getMasterHead() {
		return masterHead;
	}

	public void setMasterHead(String masterHead) {
		this.masterHead = masterHead;
	}

	public MyStarInfo() {
	}

	public List<DBObject> getSlave() {
		return slave;
	}

	public void setSlave(List<DBObject> slave) {
		this.slave = slave;
	}

	public int getSlaveLevel() {
		return slaveLevel;
	}

	public Long getWorth() {
		return worth;
	}

	public void setWorth(Long worth) {
		this.worth = worth;
	}

	public int getStarLevel() {
		return starLevel;
	}

	public Long getMasterId() {
		return masterId;
	}

	public void setSlaveLevel(int slaveLevel) {
		this.slaveLevel = slaveLevel;
	}

	public void setStarLevel(int starLevel) {
		this.starLevel = starLevel;
	}

	public void setMasterId(Long masterId) {
		this.masterId = masterId;
	}

}
