package com.zb.models;

import java.io.Serializable;

public class AppCover implements Serializable {
	private static final long serialVersionUID = 8244602077847665961L;

	private long _id;// day 201747周
	private Long uid;
	private String nickname;
	private String url;// 封面地址
	private long updateTime;
	private long adminId;

	public long get_id() {
		return _id;
	}

	public void set_id(long _id) {
		this._id = _id;
	}

	public Long getUid() {
		return uid;
	}

	public void setUid(Long uid) {
		this.uid = uid;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public long getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(long updateTime) {
		this.updateTime = updateTime;
	}

	public long getAdminId() {
		return adminId;
	}

	public void setAdminId(long adminId) {
		this.adminId = adminId;
	}

	public AppCover(long _id, Long uid, String nickname, String url, long adminId) {
		super();
		this._id = _id;
		this.uid = uid;
		this.nickname = nickname;
		this.url = url;
		this.adminId = adminId;
	}

	public AppCover() {
		super();
	}

}
