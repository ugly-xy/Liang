package com.zb.models.room.activity;

import java.io.Serializable;
import java.util.List;

//企鹅
public class Penguin implements Serializable {
	private static final long serialVersionUID = 3486235392908593943L;
	public static final int OK = 1;
	public static final int DEATH = 2;
	private int No;// 编号
	private long uid; // 用户id
	private int status = OK; // 状态
	private int color; // 颜色
	private Integer x; // 初始x坐标
	private Integer y; // 初始y坐标
	private Integer mx; // 目标坐标点x
	private Integer my; // 目标坐标点y
	private Integer ex; // 最终坐标x
	private Integer ey; // 最终坐标y
	private List path; // 路径

	public Penguin(int No, long uid, int color, int x, int y) {
		super();
		this.No = No;
		this.uid = uid;
		this.color = color;
		this.x = x;
		this.y = y;
	}

	public Penguin() {
		super();
		// TODO Auto-generated constructor stub
	}

	public long getUid() {
		return uid;
	}

	public void setUid(long uid) {
		this.uid = uid;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}

	public int getX() {
		return x;
	}

	public void setX(Integer x) {
		this.x = x;
	}

	public Integer getY() {
		return y;
	}

	public void setY(Integer y) {
		this.y = y;
	}

	public Integer getMx() {
		return mx;
	}

	public void setMx(Integer mx) {
		this.mx = mx;
	}

	public Integer getMy() {
		return my;
	}

	public void setMy(Integer my) {
		this.my = my;
	}

	public Integer getEx() {
		return ex;
	}

	public void setEx(Integer ex) {
		this.ex = ex;
	}

	public Integer getEy() {
		return ey;
	}

	public void setEy(Integer ey) {
		this.ey = ey;
	}

	public List getPath() {
		return path;
	}

	public void setPath(List path) {
		this.path = path;
	}

	public int getNo() {
		return No;
	}

	public void setNo(int no) {
		No = no;
	}
}
