package com.zb.models.room.activity;

import java.io.Serializable;

public class GuessWord implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6895731734124099392L;

	public GuessWord() {

	}

	public GuessWord(String word, int status, String tip, String provider,long uid) {
		this._id = word;
		this.status = status;
		this.tip = tip;
		this.provider = provider;
		this.uid = uid;
	}

	public long getUid() {
		return uid;
	}

	public void setUid(long uid) {
		this.uid = uid;
	}

	private String _id;
	private int status;
	private String tip;
	private String provider;
	private long uid;

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String get_id() {
		return _id;
	}

	public void set_id(String _id) {
		this._id = _id;
	}

	public String getTip() {
		return tip;
	}

	public void setTip(String tip) {
		this.tip = tip;
	}

	public String getProvider() {
		return provider;
	}

	public void setProvider(String provider) {
		this.provider = provider;
	}

}
