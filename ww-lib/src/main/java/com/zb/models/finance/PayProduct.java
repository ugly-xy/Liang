package com.zb.models.finance;

public class PayProduct {

	public static final int SEND_TYPE_NOTHING = 0;
	public static final int SEND_TYPE_AMOUNT = 1;
	public static final int SEND_TYPE_PROCENTTAGE = 2;

	public static final int LIMIT_0 = 0;// 无限制
	public static final int LIMIT_11 = 11;// 每天一次

	private String id;
	private String name;
	private int rmb;
	private int coin;
	private int sendType;
	private int sendAmount;
	private int limit;
	private int flAmount;
	private int expAmount;

	public PayProduct() {

	}

	public PayProduct(String id, String name, int rmb, int coin, int limit, int sendType, int sendAmount, int expAmount,
			int flAmount) {
		this.id = id;
		this.name = name;
		this.rmb = rmb;
		this.coin = coin;
		this.limit = limit;
		this.sendType = sendType;
		this.sendAmount = sendAmount;
		this.expAmount = expAmount;
		this.flAmount = flAmount;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getRmb() {
		return rmb;
	}

	public void setRmb(int rmb) {
		this.rmb = rmb;
	}

	public int getLimit() {
		return limit;
	}

	public void setLimit(int limit) {
		this.limit = limit;
	}

	public int getCoin() {
		return coin;
	}

	public void setCoin(int coin) {
		this.coin = coin;
	}

	public int getSendType() {
		return sendType;
	}

	public void setSendType(int sendType) {
		this.sendType = sendType;
	}

	public int getSendAmount() {
		return sendAmount;
	}

	public void setSendAmount(int sendAmount) {
		this.sendAmount = sendAmount;
	}

	public int getFlAmount() {
		return flAmount;
	}

	public void setFlAmount(int flAmount) {
		this.flAmount = flAmount;
	}

	public int getExpAmount() {
		return expAmount;
	}

	public void setExpAmount(int expAmount) {
		this.expAmount = expAmount;
	}
}
