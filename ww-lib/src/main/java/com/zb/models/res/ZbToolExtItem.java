package com.zb.models.res;

import java.util.List;

public class ZbToolExtItem {

	public ZbToolExtItem() {

	}

	public ZbToolExtItem(String id, String name, Boolean show, String type,
			Integer weight, Integer height, List<String> selects, int len) {
		this._id = id;
		this.name = name;
		this.show = show;
		this.type = type;
		this.weight = weight;
		this.height = height;
		this.selects = selects;
		this.len = len;
	}

	public String get_id() {
		return _id;
	}

	public void set_id(String _id) {
		this._id = _id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Boolean getShow() {
		return show;
	}

	public void setShow(Boolean show) {
		this.show = show;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Integer getWeight() {
		return weight;
	}

	public void setWeight(Integer weight) {
		this.weight = weight;
	}

	public Integer getHeight() {
		return height;
	}

	public void setHeight(Integer height) {
		this.height = height;
	}

	public List<String> getSelects() {
		return selects;
	}

	public void setSelects(List<String> selects) {
		this.selects = selects;
	}

	public int getLen() {
		return len;
	}

	public void setLen(int len) {
		this.len = len;
	}

	public boolean isHasExt() {
		return hasExt;
	}

	public void setHasExt(boolean hasExt) {
		this.hasExt = hasExt;
	}

	public List<String> getSelectsExt() {
		return selectsExt;
	}

	public void setSelectsExt(List<String> selectsExt) {
		this.selectsExt = selectsExt;
	}
	

	public boolean isCustom() {
		return custom;
	}

	public void setCustom(boolean custom) {
		this.custom = custom;
	}



	private String _id; //id
	private String name; 
	private Boolean show;
	private String type;
	private Integer weight; // 1 index
	private Integer height;
	private List<String> selects;
	private int len = 0;
	private boolean hasExt = false;
	private List<String> selectsExt;
	private boolean custom = false;

}
