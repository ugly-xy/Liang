package com.zb.models.lottery;

import java.io.Serializable;

public class LotteryResult implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 4316465090928212046L;
	private boolean isWin = false;
	private String widId;
	private String name;//奖品名
	private String pic;
	
	public LotteryResult(){
		
	}
	
	public LotteryResult(boolean isWin,String widId,String name,String pic){
		this.isWin = isWin;
		this.widId = widId;
		this.name = name;
		this.pic = pic;
	}

	public boolean isWin() {
		return isWin;
	}

	public void setWin(boolean isWin) {
		this.isWin = isWin;
	}

	public String getWidId() {
		return widId;
	}

	public void setWidId(String widId) {
		this.widId = widId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPic() {
		return pic;
	}

	public void setPic(String pic) {
		this.pic = pic;
	}
	
	
}