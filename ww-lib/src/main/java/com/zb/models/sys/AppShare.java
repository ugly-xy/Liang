package com.zb.models.sys;

import java.io.Serializable;

public class AppShare implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6016361050004072890L;

	private long _id;
	private int count;

	public AppShare() {

	}

	public AppShare(long id, int count) {
		this._id = id;
		this.count = count;
	}

	public long get_id() {
		return _id;
	}

	public void set_id(long _id) {
		this._id = _id;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}