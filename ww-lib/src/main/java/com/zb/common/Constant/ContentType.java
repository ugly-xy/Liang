package com.zb.common.Constant;

import java.util.HashMap;
import java.util.Map;

public class ContentType {
	static Map<String,String> m = new HashMap<String,String>();
	static{
		m.put("image/png", "png");
		m.put("application/x-png", "png");
		m.put("image/jpeg", "jpg");
		m.put("application/x-jpg", "jpg");
		m.put("image/gif", "gif");
		m.put("text/html", "html");
		m.put("text/plain", "txt");
		m.put("application/x-bmp", "bmp");
		m.put("audio/wav", "wav");
	}
	public static String get(String contentType){
		return m.get(contentType.toLowerCase());
	}
}
