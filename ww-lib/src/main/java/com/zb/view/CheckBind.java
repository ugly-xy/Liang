package com.zb.view;

import java.io.Serializable;

public class CheckBind implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 8181286615289455845L;
	public static int YES = 1;
	public static int NO = 0;

	private int phone = 0;
	private int third = 0;

	public CheckBind() {
		super();
		this.phone = NO;
		this.third = NO;
	}

	public CheckBind(int phone, int third) {
		super();
		this.phone = phone;
		this.third = third;
	}

	public int getPhone() {
		return phone;
	}

	public void setPhone(int phone) {
		this.phone = phone;
	}

	public int getThird() {
		return third;
	}

	public void setThird(int third) {
		this.third = third;
	}
}
